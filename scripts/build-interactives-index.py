#!/usr/bin/env python3
"""
interactives/index.html 자동 생성기.

각 interactives/*.html 상단 주석 frontmatter(title/date/status/category)를
단일 진실원천으로 삼아 카탈로그 페이지를 생성한다. 수기 카드 추가를 없애
도구 누락(invisible) 재발을 막는다.

실행: python3 scripts/build-interactives-index.py   (repo 루트 기준)
출력: interactives/index.html  (덮어쓰기)

category 미지정/미정의 파일은 '기타 · 미분류' 버킷으로 떨어져 절대 누락되지 않는다.
"""

import re
import sys
import html
