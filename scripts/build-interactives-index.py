def main():
    tools = collect()
    total = len(tools)

    # 카테고리별 그룹핑, 그룹 내 date 내림차순 (동률은 파일명 내림차순)
    by_cat = {}
    for t in tools:
        by_cat.setdefault(t["category"], []).append(t)
    for items in by_cat.values():
        items.sort(key=lambda x: (x["date"], x["file"]), reverse=True)

    order = CATEGORIES + [(FALLBACK_KEY, FALLBACK_LABEL)]
    sections = []
    for key, label in order:
        if by_cat.get(key):
            sections.append(render_section(label, by_cat[key]))

    header = (
        f'  <header>\n'
        f'    <h1>🦷 Interactive Tools <span class="count">{total}</span></h1>\n'
        f'    <p class="sub">LLM Wiki 치과학 — chairside 계산기·의사결정 트리·시뮬레이터 모음. '
        f'모든 도구는 wiki 근거에 연결된 자체완결 HTML입니다. 경로: <code>/interactives/</code></p>\n'
        f'  </header>'
    )

    out = HEAD + header + "\n\n" + "\n\n".join(sections) + "\n" + FOOTER
    OUT.write_text(out, encoding="utf-8")
    print(f"[build-interactives-index] {total} tools → {OUT.relative_to(ROOT)}")

    # 미분류 경고 (빌드 실패는 아님 — visibility 신호)
    misc = by_cat.get(FALLBACK_KEY, [])
    if misc:
        print(f"  WARNING: {len(misc)} tool(s) without valid category → '기타·미분류':", file=sys.stderr)
        for t in misc:
            print(f"    - {t['file']}", file=sys.stderr)


if __name__ == "__main__":
    main()
