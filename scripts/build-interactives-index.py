# 스크립트 위치 기준으로 repo 경로 해석 (cwd 무관)
ROOT = Path(__file__).resolve().parent.parent
INTERACTIVES = ROOT / "interactives"
OUT = INTERACTIVES / "index.html"

# 카테고리 표시 순서 + 한글 라벨. 키는 frontmatter의 category: 값과 일치.
CATEGORIES = [
    ("isq",       "ISQ · RFA · 부하 결정"),
    ("surgery",   "수술 · 술식 의사결정"),
    ("materials", "재료 · 접착 · 약물"),
    ("caries",    "우식 · 예방 · 미생물"),
    ("patient",   "환자 커뮤니케이션"),
    ("meta",      "메타 · 위키 성장"),
]
FALLBACK_KEY = "_misc"
FALLBACK_LABEL = "기타 · 미분류"

# status -> badge CSS class (index.html <style>에 정의됨). 미지정은 draft로.
KNOWN_STATUS = {"done", "review", "in-progress", "draft"}


def parse_frontmatter(text: str) -> dict:
    """상단 <!-- --- ... --- --> 주석 블록에서 key: value 추출."""
    m = re.search(r"<!--\s*\n---\s*\n(.*?)\n---\s*\n-->", text, flags=re.S)
    block = m.group(1) if m else text[:800]
    fm = {}
    for line in block.split("\n"):
        km = re.match(r"^([a-zA-Z_]+):\s*(.*)$", line)
        if km:
            key, val = km.group(1), km.group(2).strip()
            val = val.strip('"').strip("'")
            if key not in fm:  # 첫 등장만 (중첩 source_wiki 등 무시)
                fm[key] = val
    return fm


def collect():
    tools = []
    for p in sorted(INTERACTIVES.glob("*.html")):
        if p.name == "index.html":
            continue
        fm = parse_frontmatter(p.read_text(encoding="utf-8"))
        cat = fm.get("category", "").strip() or FALLBACK_KEY
        valid_keys = {k for k, _ in CATEGORIES}
        if cat not in valid_keys:
            cat = FALLBACK_KEY
        status = fm.get("status", "draft").strip()
        if status not in KNOWN_STATUS:
            status = "draft"
        tools.append({
            "file": p.name,
            "title": fm.get("title", p.stem),
            "date": fm.get("date", ""),
            "status": status,
            "category": cat,
        })
    return tools


def render_card(t: dict) -> str:
    title = html.escape(t["title"])
    return (
        f'      <a class="card" href="{t["file"]}">\n'
        f'        <p class="t">{title}</p>\n'
        f'        <div class="meta"><span class="date">{t["date"]}</span>'
        f'<span class="badge {t["status"]}">{t["status"]}</span></div>\n'
        f'      </a>'
    )


def render_section(label: str, items: list) -> str:
    cards = "\n".join(render_card(t) for t in items)
    return (
        f'  <section>\n'
        f'    <h2>{html.escape(label)}</h2>\n'
        f'    <div class="grid">\n{cards}\n    </div>\n'
        f'  </section>'
    )


